def monthStr(index):
  months = {
    1: 'janeiro',
    2: 'fevereiro',
    3: 'março',
    4: 'abril',
    5: 'maio',
    6: 'junho',
    7: 'julho',
    8: 'agosto',
    9: 'setembro',
    10: 'outubro',
    11: 'novembro',
    12: 'dezembro'
  }
  return months[index]

monthIndex = int(input("Digite o número do mês de 1 a 12: "))
print(monthStr(monthIndex))